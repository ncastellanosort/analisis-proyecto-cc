    private void btnReportarRoboFrameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReportarRoboFrameActionPerformed

        ErrorAlAgregar error = new ErrorAlAgregar();

        AgregadoExitosamente bien = new AgregadoExitosamente();

        try {

            int identificacionRobo = Integer.parseInt(txtfIdentificacionRobo.getText());
            String fecha = txtfFechaRobo.getText();
            String hora = txtfHoraRobo.getText();
            String descripcion = txtfDescripcionRobo.getText();
            String valor = txtfValorEStimadoRobo.getText();
            String esLocal = txtfFueLocalRobo.getText();
            String objetosR = txtfObjetosRobados.getText();
            String testigoR = txtfTestigosRobo.getText();
            String danos = txtfDanosMaterialesRobo.getText();
            String notifAutoridades = txtfNotificacionAutoridadesRobo.getText();

            ListaRobos.getRobosCentroComercial().put(identificacionRobo, new Robo(fecha, hora, descripcion, valor, esLocal, objetosR, testigoR, danos, notifAutoridades));

            bien.setLocationRelativeTo(null);
            bien.setResizable(false);
            bien.setVisible(true);

            this.dispose();

        } catch (Exception e) {

            error.setLocationRelativeTo(null);
            error.setResizable(false);
            error.setVisible(true);

        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btnReportarRoboFrameActionPerformed

