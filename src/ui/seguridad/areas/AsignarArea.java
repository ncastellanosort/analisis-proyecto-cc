    private void btnAgregarTareaFrameTareasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarTareaFrameTareasActionPerformed

        ErrorAlAgregar error = new ErrorAlAgregar();

        AgregadoExitosamente bien = new AgregadoExitosamente();

        try {

            int identificacionArea = Integer.parseInt(txtfNumIdentificacionArea.getText());
            String fechaInput = txtfFechaArea.getText();
            String nombreAreaInput = txtfNombreArea.getText();
            String ubicacionAreaInput = txtfUbicacionArea.getText();
            String tamanoAreaInput = txtfTamanoArea.getText();
            String nombreEncargadoAreaInput = txtfNombreEncargadoArea.getText();
            int cedulaEncargadoAreaInput = Integer.parseInt(txtfCedulaEncargado.getText());
            int horaInicioEncargadoInput = Integer.parseInt(txtfHoraInicio.getText());
            int horaFinEncargadoInput = Integer.parseInt(txtfHoraFin.getText());
            String pisoAreaInput = txtfPisoArea.getText();
            String finalizadoInput = txtfFinalizadoArea.getText();

            ListaAreas.getAreasCentroComercial().put(identificacionArea, new Area(fechaInput, nombreAreaInput, ubicacionAreaInput, tamanoAreaInput, nombreEncargadoAreaInput, cedulaEncargadoAreaInput, horaInicioEncargadoInput, horaFinEncargadoInput, pisoAreaInput, finalizadoInput));

            bien.setLocationRelativeTo(null);
            bien.setResizable(false);
            bien.setVisible(true);

            this.dispose();

        } catch (Exception e) {

            error.setLocationRelativeTo(null);
            error.setResizable(false);
            error.setVisible(true);

        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btnAgregarTareaFrameTareasActionPerformed

