    private void btnReportarIncidenteFrameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReportarIncidenteFrameActionPerformed

        ErrorAlAgregar error = new ErrorAlAgregar();

        AgregadoExitosamente bien = new AgregadoExitosamente();

        try {

            int identificacionIncidente = Integer.parseInt(txtfNumIdentificacionIncidente.getText());

            String nombreAreaIncidente = txtfAreaIncidente.getText();
            String fechaIncidente = txtfFechaIncidente.getText();
            String horaIncidente = txtfHoraINcidente.getText();
            String descripcionIncidente = txtfDescripcionINcidente.getText();
            String personasInvolucradasIncidente = txtfPersonasInvolucradas.getText();
            String accionesIncidente = txtfAccionesTomadasIncidente.getText();
            String danosMateIncidente = txtfDanosMaterialesINcidente.getText();
            String notificacionIncidente = txtfNotificacionAutorIncidente.getText();

            ListaIncidentes.getIncidentesCentroComercial().put(identificacionIncidente, new Incidente(nombreAreaIncidente, fechaIncidente, horaIncidente, descripcionIncidente, personasInvolucradasIncidente, accionesIncidente, danosMateIncidente, notificacionIncidente));

            bien.setLocationRelativeTo(null);
            bien.setResizable(false);
            bien.setVisible(true);

            this.dispose();

        } catch (Exception e) {

            error.setLocationRelativeTo(null);
            error.setResizable(false);
            error.setVisible(true);

        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btnReportarIncidenteFrameActionPerformed

