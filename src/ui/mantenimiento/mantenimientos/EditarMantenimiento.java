    private void btnEditarMatenimientoNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarMatenimientoNuevoActionPerformed

        ListaMantenimientos.getMantenimientosCentroComercial().get(IDeditar).setNombreMantenimiento(txtfNUEVONombreMantenimiento.getText());

        ListaMantenimientos.getMantenimientosCentroComercial().get(IDeditar).setEncargadoMantenimiento(txtfNUEVOPersonaEncargadaMantenimiento.getText());

        ListaMantenimientos.getMantenimientosCentroComercial().get(IDeditar).setCedulaEncargado(Integer.parseInt(txtfNUEVOCedulaPersonaMantenimiento.getText()));

        ListaMantenimientos.getMantenimientosCentroComercial().get(IDeditar).setDescripcionMantenimiento(txtfNUEVODescripcionMantenimiento.getText());

        ListaMantenimientos.getMantenimientosCentroComercial().get(IDeditar).setFinalizadoMantenimiento(txtfNUEVOFinalizadoMantenimiento.getText());

        ListaMantenimientos.getMantenimientosCentroComercial().get(IDeditar).setZonaMantenimiento(txtfNUEVOZonaMantenimiento.getText());
        ListaMantenimientos.getMantenimientosCentroComercial().get(IDeditar).setHoraFinalizacion(txtfNUEVOHoraFinalizacionMantenimiento.getText());
        ListaMantenimientos.getMantenimientosCentroComercial().get(IDeditar).setFechaFinalizacion(txtfNUEVOFechaFinalizacionMantenimiento.getText());

        EditadoExitosamente editado = new EditadoExitosamente();

        this.dispose();

        editado.setLocationRelativeTo(null);
        editado.setResizable(false);
        editado.setVisible(true);

        // TODO add your handling code here:
    }//GEN-LAST:event_btnEditarMatenimientoNuevoActionPerformed

