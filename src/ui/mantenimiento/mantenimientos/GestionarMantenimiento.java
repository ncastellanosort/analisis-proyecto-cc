    private void btnBuscarMantenimientoIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarMantenimientoIDActionPerformed

        int ID = Integer.parseInt(txtfBuscarMantenimiento.getText());

        lbFinalizadoMantenimiento.setText(ListaMantenimientos.getMantenimientosCentroComercial().get(ID).getFinalizadoMantenimiento());

        Object[] ob = {ID, ListaMantenimientos.getMantenimientosCentroComercial().get(ID).getNombreMantenimiento(), ListaMantenimientos.getMantenimientosCentroComercial().get(ID).getEncargadoMantenimiento(), ListaMantenimientos.getMantenimientosCentroComercial().get(ID).getCedulaEncargado(), ListaMantenimientos.getMantenimientosCentroComercial().get(ID).getDescripcionMantenimiento(), ListaMantenimientos.getMantenimientosCentroComercial().get(ID).getFinalizadoMantenimiento(), ListaMantenimientos.getMantenimientosCentroComercial().get(ID).getZonaMantenimiento(), ListaMantenimientos.getMantenimientosCentroComercial().get(ID).getHoraFinalizacion(), ListaMantenimientos.getMantenimientosCentroComercial().get(ID).getFechaFinalizacion()};

        modelo.addRow(ob);
        Utilidades.escalarLargoTablas(columnas.length, tblUnicoMantenimiento);

        // TODO add your handling code here:
    }//GEN-LAST:event_btnBuscarMantenimientoIDActionPerformed

    private void btnEliminarMantenimientoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarMantenimientoActionPerformed
        int ID = Integer.parseInt(txtfBuscarMantenimiento.getText());

        ListaMantenimientos.getMantenimientosCentroComercial().remove(ID);

        this.dispose();
        EliminadoExitosamente elim = new EliminadoExitosamente();

        elim.setLocationRelativeTo(null);
        elim.setResizable(false);
        elim.setVisible(true);
    }//GEN-LAST:event_btnEliminarMantenimientoActionPerformed

    private void btnEditarMantenimientoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarMantenimientoActionPerformed

        int ID = Integer.parseInt(txtfBuscarMantenimiento.getText());

        EditarMantenimiento editarMantenimiento = new EditarMantenimiento();

        editarMantenimiento.setIDeditar(ID);

        editarMantenimiento.getTxtfNUEVONombreMantenimiento().setText(ListaMantenimientos.getMantenimientosCentroComercial().get(ID).getNombreMantenimiento());

        editarMantenimiento.getTxtfNUEVOPersonaEncargadaMantenimiento().setText(ListaMantenimientos.getMantenimientosCentroComercial().get(ID).getEncargadoMantenimiento());

        editarMantenimiento.getTxtfNUEVOCedulaPersonaMantenimiento().setText(String.valueOf(ListaMantenimientos.getMantenimientosCentroComercial().get(ID).getCedulaEncargado()));

        editarMantenimiento.getTxtfNUEVODescripcionMantenimiento().setText(ListaMantenimientos.getMantenimientosCentroComercial().get(ID).getDescripcionMantenimiento());

        editarMantenimiento.getTxtfNUEVOFechaFinalizacionMantenimiento().setText(ListaMantenimientos.getMantenimientosCentroComercial().get(ID).getFinalizadoMantenimiento());
        editarMantenimiento.getTxtfNUEVOZonaMantenimiento().setText(ListaMantenimientos.getMantenimientosCentroComercial().get(ID).getZonaMantenimiento());
        editarMantenimiento.getTxtfNUEVOHoraFinalizacionMantenimiento().setText(ListaMantenimientos.getMantenimientosCentroComercial().get(ID).getHoraFinalizacion());
        editarMantenimiento.getTxtfNUEVOFechaFinalizacionMantenimiento().setText(ListaMantenimientos.getMantenimientosCentroComercial().get(ID).getFechaFinalizacion());
        editarMantenimiento.getLbNumIdMantenimiento().setText(String.valueOf(ID));

        this.dispose();

        editarMantenimiento.setLocationRelativeTo(null);
        editarMantenimiento.setResizable(false);
        editarMantenimiento.setVisible(true);
        // APENAS LE DE ACA A TODOS LOS TXTFIELD PONERLE LOS GETTER
        // TODO add your handling code here:
    }//GEN-LAST:event_btnEditarMantenimientoActionPerformed

