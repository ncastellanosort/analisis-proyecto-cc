    private void btnAgendarMantenimientoFrameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgendarMantenimientoFrameActionPerformed

        ErrorAlAgregar error = new ErrorAlAgregar();

        AgregadoExitosamente bien = new AgregadoExitosamente();

        try {

            int identificacionMantenimiento = Integer.parseInt(txtfNumIdentificacionMantenimiento.getText());
            String nombreMantenimiento = txtfNombreMantenimiento.getText();
            String encargado = txtfPersonaEncargadaMantenimiento.getText();
            int cedula = Integer.parseInt(txtfCedulaPersonaMantenimiento.getText());
            String descripcion = txtfDescripcionMantenimiento.getText();
            String finalizado = txtfFinalizadoMantenimiento.getText();
            String zona = txtfZonaMantenimiento.getText();
            String horaF = txtfHoraFinalizacionMantenimiento.getText();
            String fechaF = txtfFechaFinalizacionMantenimiento.getText();

            ListaMantenimientos.getMantenimientosCentroComercial().put(identificacionMantenimiento, new Mantenimiento(nombreMantenimiento, encargado, cedula, descripcion, finalizado, zona, horaF, fechaF));

            bien.setLocationRelativeTo(null);
            bien.setResizable(false);
            bien.setVisible(true);

            this.dispose();

        } catch (Exception e) {

            error.setLocationRelativeTo(null);
            error.setResizable(false);
            error.setVisible(true);

        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btnAgendarMantenimientoFrameActionPerformed

