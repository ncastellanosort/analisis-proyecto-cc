    private void btnEDITARproveedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEDITARproveedorActionPerformed

        EditadoExitosamente ed = new EditadoExitosamente();

        try {

            String nuevoNombre = txtfNUEVONombreProveedor.getText();
            String nuevaDireccionRegistrada = txtfNUEVODireccionRegistrada.getText();
            long nuevoNiumCont = Integer.parseInt(txtfNUEVONumeroContactoProveedor.getText());
            String nuevoCorreo = txtfNUEVODireccionCorreoProveedor.getText();
            String nuevoServicio = txtfNUEVOServicioSuministradoProveedor.getText();
            String nuevaReferencia = txtfNUEVOReferenciaCProveedor.getText();
            String nuevoTermPago = txtfNUEVOTerminoDePagoProveedor.getText();

            ListaProveedores.getProveedoresCentroComercial().get(INDICE).setNombreEmpresaProveedor(nuevoNombre);
            ListaProveedores.getProveedoresCentroComercial().get(INDICE).setDireccionRegistradaProveedor(nuevaDireccionRegistrada);
            ListaProveedores.getProveedoresCentroComercial().get(INDICE).setNumeroContactoProveedor(nuevoNiumCont);
            ListaProveedores.getProveedoresCentroComercial().get(INDICE).setDireccionCorreoProveedor(nuevoCorreo);
            ListaProveedores.getProveedoresCentroComercial().get(INDICE).setServicioSuministradoProveedor(nuevoServicio);
            ListaProveedores.getProveedoresCentroComercial().get(INDICE).setReferenciaComercialProveedor(nuevaReferencia);
            ListaProveedores.getProveedoresCentroComercial().get(INDICE).setTerminoDePagoProveedor(nuevoTermPago);

            this.dispose();
            ed.setLocationRelativeTo(null);
            ed.setResizable(false);
            ed.setVisible(true);

        } catch (Exception e) {

        }

//        this.dispose();
        // TODO add your handling code here:
    }//GEN-LAST:event_btnEDITARproveedorActionPerformed

