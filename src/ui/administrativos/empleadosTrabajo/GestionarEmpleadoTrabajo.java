    private void btnBuscarHorarioEmpleadoIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarHorarioEmpleadoIDActionPerformed

        int ID = Integer.parseInt(txtfCedulaHorarioEmpleado.getText());

        Object[] ob = {ID, ListaEmpleadosHorarios.getHorariosEmpleados().get(ID).getFecha(), ListaEmpleadosHorarios.getHorariosEmpleados().get(ID).getNombreEmpleadoRonda(), ListaEmpleadosHorarios.getHorariosEmpleados().get(ID).getAreaEmpleado(), ListaEmpleadosHorarios.getHorariosEmpleados().get(ID).getHoraEntrada(), ListaEmpleadosHorarios.getHorariosEmpleados().get(ID).getHoraSalida(), ListaEmpleadosHorarios.getHorariosEmpleados().get(ID).getHorasExtra()};

        modelo.addRow(ob);
        Utilidades.escalarLargoTablas(columnasEmpleadosTabla.length, tblUnicoHorarioEmpleado);

        // TODO add your handling code here:
    }//GEN-LAST:event_btnBuscarHorarioEmpleadoIDActionPerformed

    private void btnEliminarHorarioEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarHorarioEmpActionPerformed
        int ID = Integer.parseInt(txtfCedulaHorarioEmpleado.getText());

        ListaEmpleadosHorarios.getHorariosEmpleados().remove(ID);

        this.dispose();
        EliminadoExitosamente elim = new EliminadoExitosamente();

        elim.setLocationRelativeTo(null);
        elim.setResizable(false);
        elim.setVisible(true);
    }//GEN-LAST:event_btnEliminarHorarioEmpActionPerformed

    private void btnEditarHorarioEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarHorarioEmpActionPerformed

        int ID = Integer.parseInt(txtfCedulaHorarioEmpleado.getText());
        
        

        EditarHorarioPersonal editarHorarioPersonal = new EditarHorarioPersonal();
        
        editarHorarioPersonal.setIDeditar(ID);

        editarHorarioPersonal.getTxtfNUEVOHoraEntrada().setText(ListaEmpleadosHorarios.getHorariosEmpleados().get(ID).getHoraEntrada());
        editarHorarioPersonal.getTxtfNUEVOHoraSalida().setText(ListaEmpleadosHorarios.getHorariosEmpleados().get(ID).getHoraSalida());
        editarHorarioPersonal.getTxtfNUEVOHorasExtra().setText(ListaEmpleadosHorarios.getHorariosEmpleados().get(ID).getHorasExtra());

        editarHorarioPersonal.getLbNumeroIdentificacion().setText(String.valueOf(ID));
        editarHorarioPersonal.getLbFecha().setText(ListaEmpleadosHorarios.getHorariosEmpleados().get(ID).getFecha());
        editarHorarioPersonal.getLbNombreEm().setText(ListaEmpleadosHorarios.getHorariosEmpleados().get(ID).getNombreEmpleadoRonda());
        editarHorarioPersonal.getLbAreaEmp().setText(ListaEmpleadosHorarios.getHorariosEmpleados().get(ID).getAreaEmpleado());

        this.dispose();

        editarHorarioPersonal.setLocationRelativeTo(null);
        editarHorarioPersonal.setResizable(false);
        editarHorarioPersonal.setVisible(true);
        // APENAS LE DE ACA A TODOS LOS TXTFIELD PONERLE LOS GETTER
        // TODO add your handling code here:
    }//GEN-LAST:event_btnEditarHorarioEmpActionPerformed

