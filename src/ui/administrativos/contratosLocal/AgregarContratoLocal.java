    private void btnRegistrarContratoFrameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarContratoFrameActionPerformed

        ErrorAlAgregar error = new ErrorAlAgregar();

        AgregadoExitosamente bien = new AgregadoExitosamente();

        try {

            int identificacionContratoLocal = Integer.parseInt(txtfNumeroIDcontratoLocal.getText());
            String nombreArrendatario = txtfNombreArrendatarioLocal.getText();
            String cedulaArrendatario = txtfCedulaArrendatario.getText();
            String descripcion = txtfDescripcionContrato.getText();
            String duracionContrato = txtfDuracionContrato.getText();
            String condiciones = txtfCondicionesRenovacion.getText();
            int montoMensual = Integer.parseInt(txtfMontoMensual.getText());
            String plazoPago = txtfPLazosPago.getText();
            String uso = txtfUsoLocal.getText();
            String nombreLocal = txtfNombreLocal.getText();
            String segurosLocal = txtfSegurosLocal.getText();

            ListaContratosLocales.getContratosCentroComercial().put(identificacionContratoLocal, new ContratoLocal(nombreArrendatario, cedulaArrendatario, descripcion, duracionContrato, condiciones, montoMensual, plazoPago, uso, nombreLocal, segurosLocal));

            bien.setLocationRelativeTo(null);
            bien.setResizable(false);
            bien.setVisible(true);

            this.dispose();

        } catch (Exception e) {

            error.setLocationRelativeTo(null);
            error.setResizable(false);
            error.setVisible(true);

        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btnRegistrarContratoFrameActionPerformed

