    private void btnBuscarContratoLocalIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarContratoLocalIDActionPerformed

        int ID = Integer.parseInt(txtfBuscarContratoLocal.getText());

        Object[] ob = {ID, ListaContratosLocales.getContratosCentroComercial().get(ID).getNombreArrendatarioContrato(), ListaContratosLocales.getContratosCentroComercial().get(ID).getCedulaArrendatario(), ListaContratosLocales.getContratosCentroComercial().get(ID).getDescripcionContrato(), ListaContratosLocales.getContratosCentroComercial().get(ID).getDuracionContrato(), ListaContratosLocales.getContratosCentroComercial().get(ID).getCondicionesDeRenovacionContrato(), ListaContratosLocales.getContratosCentroComercial().get(ID).getMontoMensualContrato(), ListaContratosLocales.getContratosCentroComercial().get(ID).getPlazosDePagoContrato(), ListaContratosLocales.getContratosCentroComercial().get(ID).getUsoDelLocalContrato(), ListaContratosLocales.getContratosCentroComercial().get(ID).getNombreLocalContrato(), ListaContratosLocales.getContratosCentroComercial().get(ID).getSeguros()};

        modelo.addRow(ob);
        escalarLargoTablas(columnasTabla.length, tblUnicoContratoLocal);

        // TODO add your handling code here:
    }//GEN-LAST:event_btnBuscarContratoLocalIDActionPerformed

    private void btnEliminarContratoLocalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarContratoLocalActionPerformed
        int ID = Integer.parseInt(txtfBuscarContratoLocal.getText());

        ListaContratosLocales.getContratosCentroComercial().remove(ID);

        this.dispose();
        EliminadoExitosamente elim = new EliminadoExitosamente();

        elim.setLocationRelativeTo(null);
        elim.setResizable(false);
        elim.setVisible(true);
    }//GEN-LAST:event_btnEliminarContratoLocalActionPerformed

    private void btnEditarLContratoLocalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarLContratoLocalActionPerformed

        int ID = Integer.parseInt(txtfBuscarContratoLocal.getText());

        EditarContratoLocal editarContratoLocal = new EditarContratoLocal();

        editarContratoLocal.getLbIdentificacionContratoLocal().setText(String.valueOf(ID));

        editarContratoLocal.getTxtfNUEVOnombreArrendatario().setText(ListaContratosLocales.getContratosCentroComercial().get(ID).getNombreArrendatarioContrato());
        editarContratoLocal.getTxtfNUEVOCedulaArrendatario().setText(ListaContratosLocales.getContratosCentroComercial().get(ID).getCedulaArrendatario());
        editarContratoLocal.getTxtfNUEVODEscripcionContrato().setText(ListaContratosLocales.getContratosCentroComercial().get(ID).getDescripcionContrato());
        editarContratoLocal.getTxtfNUEVODuracionContrato().setText(ListaContratosLocales.getContratosCentroComercial().get(ID).getDuracionContrato());
        editarContratoLocal.getTxtfNUEVOCondicionesRenovacion().setText(ListaContratosLocales.getContratosCentroComercial().get(ID).getCondicionesDeRenovacionContrato());
        editarContratoLocal.getTxtfNUEVOMontoMensual().setText(String.valueOf(ListaContratosLocales.getContratosCentroComercial().get(ID).getMontoMensualContrato()));
        editarContratoLocal.getTxtfNUEVOPlazosPago().setText(ListaContratosLocales.getContratosCentroComercial().get(ID).getPlazosDePagoContrato());
        editarContratoLocal.getTxtfNUEVONombreLocal().setText(ListaContratosLocales.getContratosCentroComercial().get(ID).getNombreLocalContrato());
        editarContratoLocal.getTxtfNUEVOSegurosLocal().setText(ListaContratosLocales.getContratosCentroComercial().get(ID).getSeguros());

        editarContratoLocal.setIDeditar(ID);

        this.dispose();

        editarContratoLocal.setLocationRelativeTo(null);
        editarContratoLocal.setResizable(false);
        editarContratoLocal.setVisible(true);
        // APENAS LE DE ACA A TODOS LOS TXTFIELD PONERLE LOS GETTER
        // TODO add your handling code here:
    }//GEN-LAST:event_btnEditarLContratoLocalActionPerformed

