    private void btnAgregarTareaFrameTareasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarTareaFrameTareasActionPerformed

        ErrorAlAgregar error = new ErrorAlAgregar();

        AgregadoExitosamente bien = new AgregadoExitosamente();

        try {

            int identificacionLimpieza = Integer.parseInt(txtfNumIdentificacionLimpieza.getText());
            String nombreTarea = txtfNombreLimpieza.getText();
            String personaEncargada = txtfPersonaEncargada.getText();
            String tareaRealizda = txtfFinalizadaSioNo.getText();
            String descripcionTarea = txtfDescripcionDeTarea.getText();
            String areaTarea = txtfAreaTarea.getText();

            ListaTareas.getTareasCentroComercial().put(identificacionLimpieza, new Tarea(nombreTarea, personaEncargada, tareaRealizda, descripcionTarea, areaTarea));

            bien.setLocationRelativeTo(null);
            bien.setResizable(false);
            bien.setVisible(true);

            this.dispose();

        } catch (Exception e) {

            error.setLocationRelativeTo(null);
            error.setResizable(false);
            error.setVisible(true);

        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btnAgregarTareaFrameTareasActionPerformed

